package PATTERNS;

public class Patern1 {
	public static void main(String[] args) {
		
		// 1st loop - used to represent no of rows    -  i loop
		
		// 2nd loop - used to represent no of columns -  j loop 
		
		
		int n=5;
		for(int i=1;i<=n;i++)
		{
			for(int j=1;j<=n;j++)
			{
				System.out.print(i+" ");
			}
			System.out.println();
		}
	}
}
